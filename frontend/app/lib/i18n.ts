function dictionaryFor(locale: Locale): Dictionary {
  return locale === 'en' ? EN : SL;
}

export function getStoredLocale(): Locale {
  if (typeof window === 'undefined') return 'sl';
  const raw = window.localStorage.getItem(STORAGE_KEY);
  return raw === 'en' ? 'en' : 'sl';
}

export function saveStoredLocale(locale: Locale) {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(STORAGE_KEY, locale);
  window.dispatchEvent(new Event(LOCALE_UPDATED_EVENT));
}

function subscribeToLocale(callback: () => void) {
  window.addEventListener('storage', callback);
  window.addEventListener(LOCALE_UPDATED_EVENT, callback);
  return () => {
    window.removeEventListener('storage', callback);
    window.removeEventListener(LOCALE_UPDATED_EVENT, callback);
  };
}

function getLocaleSnapshot() {
  return getStoredLocale();
}

function getServerLocaleSnapshot() {
  return 'sl';
}

export function useLocale(): Locale {
  return useSyncExternalStore(
    subscribeToLocale,
    getLocaleSnapshot,
    getServerLocaleSnapshot,
  ) as Locale;
}

export function useT() {
  const locale = useLocale();
  const dict = useMemo(() => dictionaryFor(locale), [locale]);
  return useCallback(
    (key: string, fallback?: string) => dict[key] ?? fallback ?? key,
    [dict],
  );
}
